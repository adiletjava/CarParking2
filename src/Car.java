public class Car {
    private String carNumber;
    private String type;
    private String brand;

    public Car(String carNumber, String type, String brand) {
        this.carNumber = carNumber;
        this.type = type;
        this.brand = brand;
    }

    public String getCarNumber() {
        return carNumber;
    }

    public String getType() {
        return type;
    }

    public String getBrand() {
        return brand;
    }


}

